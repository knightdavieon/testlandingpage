                        <h2 class="text-black mt-0">Safety Protocols</h2>
                        <hr class="divider divider-light" />
                        <p class="text-black-75 mb-4">Nuclear power plants adhere to rigorous safety protocols to ensure the safe generation of electricity and to protect both plant personnel and the surrounding environment. Here are some key safety protocols typically in place:</p>
                </div>
            </div>
            <div class="accordion" id="accordionExample">
                <div class="accordion-item">
                  <h2 class="accordion-header">
                    <button class="accordion-button" type="button" data-bs-toggle="collapse" data-bs-target="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
                        <strong>Regulatory Compliance</strong>
                    </button>
                  </h2>
                  <div id="collapseOne" class="accordion-collapse collapse show" data-bs-parent="#accordionExample">
                    <div class="accordion-body">
                        <p class="text-black-75 mb-4">•	Nuclear Regulatory Commission (NRC) (in the U.S.) or similar regulatory bodies in other countries set strict safety standards and conduct regular inspections.</p>
                        <p class="text-black-75 mb-4">•	Plants must comply with regulations concerning reactor design, operational procedures, and emergency preparedness.</p>
                    </div>
                  </div>
                </div>

                <div class="accordion-item">
                  <h2 class="accordion-header">
                    <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
                        <strong>Reactor Design and Engineering</strong>
                    </button>
                  </h2>
                  <div id="collapseTwo" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                    <div class="accordion-body">
                        <p class="text-black-75 mb-4">•	Redundancy and Diversity: Critical systems have multiple backups and diverse methods to prevent failure. This includes redundant cooling systems, power supplies, and control systems.</p>
                        <p class="text-black-75 mb-4">•	Containment Structure: The reactor is housed within a robust containment structure designed to prevent the release of radiation in the event of an accident.</p>
                    </div>
                  </div>
                </div>

                <div class="accordion-item">
                    <h2 class="accordion-header">
                      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
                        <strong>Security Measureas</strong>
                      </button>
                    </h2>
                    <div id="collapseThree" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                      <div class="accordion-body">
                        <p class="text-black-75 mb-4">•	Physical Security: Robust physical security measures protect against unauthorized access and potential threats.</p>
                        <p class="text-black-75 mb-4">•	Cybersecurity: Protection of digital systems controlling plant operations to prevent cyberattacks.</p>
                      </div>
                    </div>
                </div>
                  
                <div class="accordion-item">
                    <h2 class="accordion-header">
                      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#collapsefour" aria-expanded="false" aria-controls="collapsefour">
                        <strong>Operational Protocols</strong>
                      </button>
                    </h2>
                    <div id="collapsefour" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
                      <div class="accordion-body">
                          <p class="text-black-75 mb-4">•	Routine Maintenance: Regular checks and maintenance are performed to ensure all systems are functioning correctly.</p>
                          <p class="text-black-75 mb-4">• Operator Training: Personnel undergo rigorous training and simulations to prepare for normal operations and emergency scenarios.</p>
                      </div>
                    </div>
                </div>

